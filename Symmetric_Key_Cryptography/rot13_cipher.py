# modified version of caesar cipher which only rotates a given string by 13 places
def rot13(s):
    result = ""

    # Loop over characters.
    for v in s:
        # Convert to number with ord.
        c = ord(v)

        # Shift number back or forward.
        if c >= ord('a') and c <= ord('z'):
            if c > ord('m'):
                c -= 13
            else:
                c += 13
        elif c >= ord('A') and c <= ord('Z'):
            if c > ord('M'):
                c -= 13
            else:
                c += 13

        # Append to result.
        result += chr(c)

    # Return transformation.
    return result

# driver code takes input and displays the rotated the text
text=str(input("ENTER A TEXT TO BE ROTATED: "))
print ("CIPHER TEXT : ", rot13(text))